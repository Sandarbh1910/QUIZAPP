private void getExamList()
{
    try{
        examList=PerformanceDAO.getExamList(UserProfile.getUsername());
        if(examList.size()==0)
        {
            JOptionPane.showMessageDialog(null, "!!! You have not appeared for any test","!!TEST NOT GIVEN",JOptionPane.INFORMATION_MESSAGE);
                StudentOptionFrame studentFrame=new StudentOptionFrame();
                studentFrame.setVisible(true);
                this.dispose();
        }
        
        
        for(String exam:examList)
        {
            cbSelectExam.addItem(exam);
        }
        cbSelectExam.setSelectedIndex(0);
    }
    catch(SQLException ex)
    {
        ex.printStackTrace();
    }
}
