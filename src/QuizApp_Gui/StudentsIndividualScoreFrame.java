    private void cbSelectExamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSelectExamActionPerformed
        // TODO add your handling code here:
        String ExamId=cbSelectExam.getSelectedItem().toString();
        try{
            StudentScore score=PerformanceDAO.getPerformanceDetatils(UserProfile.getUsername(), ExamId);
            txtSubject.setText(score.getSubject());
            txtPercentage.setText(String.valueOf(score.getPerc()));
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "!!! DB Error","!! STUDENTS INDIVIDUAL SCORE GET PERFORMANCE DETAILS ERROR",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cbSelectExamActionPerformed

