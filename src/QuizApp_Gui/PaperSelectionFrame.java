    private boolean validateInput()
    {
         int selectedIndex=cbSelectSubject.getSelectedIndex();
        if(selectedIndex==0)
        {
            return false;
        }
        subjectName=cbSelectSubject.getSelectedItem().toString();
        System.out.println("In Paper Select validInput subject = "+subjectName);
        return true;
    }
    
    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        // TODO add your handling code here:
        boolean result=validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int count=cbExamId.getItemCount();
        if(count==0)
        {
             JOptionPane.showMessageDialog(null,"Please select a exam","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        String examId=cbExamId.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null,"You have selected "+examId+" of subject "+subjectName+" ?","CONFIRM?",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try{
                int totalQuestion=ExamDAO.getQuestionCountByExam(examId);
                Exam exam=new Exam(examId,subjectName,totalQuestion);
                System.out.println("In Paper select take test "+exam);
                TakeTestFrame takeTestFrame=new TakeTestFrame(exam);
                takeTestFrame.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"DB Error","!! PAPER SELECTION ERROR", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnTakeTestActionPerformed

