    private void cbSelectSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSelectSubjectActionPerformed
        // TODO add your handling code here:
        cbExamId.removeAllItems();

        boolean result=validateInput();
        if(result==false)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            System.out.println("In paper selec ispaperset= "+ExamDAO.isPaperSet(subjectName));
            if(ExamDAO.isPaperSet(subjectName)==false)
            {
                JOptionPane.showMessageDialog(null,"Sorry no question set available for selected subject","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
            }
            
            
            ArrayList<String>examList=ExamDAO.getExamIdBySubject(subjectName,UserProfile.getUsername());
            
            if(examList.isEmpty())
            {
                cbSelectSubject.setSelectedIndex(0);
                JOptionPane.showMessageDialog(null,"No  further exams scheduled for the selected subject ","!NO EXAMS RAMAINING", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

           
            for(String examId:examList)
            {
                cbExamId.addItem(examId);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","!!PAPER SELECTION  ERROR", JOptionPane.ERROR_MESSAGE);

            ex.printStackTrace();
        }

    }//GEN-LAST:event_cbSelectSubjectActionPerformed

