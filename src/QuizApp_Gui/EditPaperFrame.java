    private boolean validateInput()
    {
        int selectedIndex=cbSelectSubject.getSelectedIndex();
        if(selectedIndex==0)
        {
            return false;
        }
        subjectName=cbSelectSubject.getSelectedItem().toString();
        return true;
    }
    
    
    private void cbSelectSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSelectSubjectActionPerformed
        // TODO add your handling code here:
        
        boolean result=validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            ArrayList<String>examList=ExamDAO.getExamIdBySubject(subjectName);
            if(examList.isEmpty())
            {
                cbSelectSubject.setSelectedIndex(0);
               JOptionPane.showMessageDialog(null,"No  exam scheduled for the selected subject","!QUESTION SET NOT FOUND", JOptionPane.ERROR_MESSAGE);
            return; 
            }
            
            cbExamId.removeAllItems();
            for(String examId:examList)
            {
                cbExamId.addItem(examId);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","!!EDIT PAPER ERROR", JOptionPane.ERROR_MESSAGE);
          
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_cbSelectSubjectActionPerformed

