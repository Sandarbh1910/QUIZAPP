    private void btnEdittQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdittQuestionsActionPerformed
        // TODO add your handling code here:
         boolean result=validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","!!ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String subject=cbSelectSubject.getSelectedItem().toString();
        String examId=cbExamId.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null,"Are you sure your wanna edit "+examId+" of subject "+subject+" ?","CONFIRM?",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try{
                int totalQuestion=ExamDAO.getQuestionCountByExam(examId);
                Exam editExam=new Exam(examId,subject,totalQuestion);
                System.out.println("BEFORE EDITQUESTION FRame");
                EditQuestionFrame editQuestionFrame=new EditQuestionFrame(editExam);
                editQuestionFrame.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex){
                 JOptionPane.showMessageDialog(null,"DB Error","!!EDIT PAPER ERROR", JOptionPane.ERROR_MESSAGE);
                 ex.printStackTrace();
            }
        }
        
        
       

    }//GEN-LAST:event_btnEdittQuestionsActionPerformed

